insert into public.fixtures (
  season_id,
  gameweek_id,
  original_gameweek_id,
  home_team_id,
  away_team_id,
  kickoff_at,
  sort_order,
  status,
  admin_note
)
select
  target_season.id,
  gw.id,
  gw.id,
  home_team.id,
  away_team.id,
  fixture_seed.kickoff_at,
  fixture_seed.sort_order,
  'scheduled',
  'Temporary 15:00 kickoff imported from workbook; update manually when real kickoff time is known.'
from fixture_seed
cross join target_season
join public.gameweeks gw
  on gw.season_id = target_season.id
 and gw.number = fixture_seed.gameweek_number
join public.teams home_team
  on home_team.name = fixture_seed.home_team_name
join public.teams away_team
  on away_team.name = fixture_seed.away_team_name
where not exists (
  select 1
  from public.fixtures existing
  where existing.season_id = target_season.id
    and existing.gameweek_id = gw.id
    and existing.home_team_id = home_team.id
    and existing.away_team_id = away_team.id
);

with target_season as (
  select id
  from public.seasons
  where name = 'Premier League 2025-26'
  order by created_at desc
  limit 1
)
select
  gw.number as gameweek,
  count(f.id) as fixtures,
  min(f.kickoff_at) as first_kickoff,
  max(f.kickoff_at) as last_kickoff
from target_season
join public.gameweeks gw on gw.season_id = target_season.id
left join public.fixtures f on f.gameweek_id = gw.id
group by gw.number
order by gw.number;
